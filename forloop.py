#!/usr/bin/env python3

for item in "Python":
    print(item)
print("")   # To leave a empty line..

for item in ["Mosh", "John", "Sarah"]:
    print(item)
print("")

for item in [1, 2, 3, 4]:
    print(item)
print("")

for item in range(10):  # range 10 will print out 0 to 9, not including 10.
    print(item)
print("")

for item in range(5, 10):   # range 5, 10 will print out 5 to 9, not 10.
    print(item)
print("")

for item in range(5, 10, 2):   # range 5, 10 with step 2 will print out 5, 7, 9.
    print(item)
print("")

