#!/usr/bin/env python3

# How to use Python's built-in module.

import math                 # Import the 'math module.

print(math.sqrt(100))       # Using the .sqrt method from the math module to calc the square root of 100.

print(help(math.sqrt))      # Using the 'help()' func to show how to use the 'math.sqrt' mnethod.

print(dir(math))            # Using the 'dir()' func to display all the available methods from the 'math' module.

