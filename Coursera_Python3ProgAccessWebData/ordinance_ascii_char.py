#!/usr/bin/env python3

""" The ord() func gives us the numeric value of a simple ASCII character. """

print(ord("H"))

print(ord("e"))

print(ord("\n"))
print("")


"""
What word does the following sequence of numbers represent in ASCII:
108, 105, 110, 101
108, 105, 115, 116
"""

print(ord("l"), ord("i"), ord("n"), ord("e"))
print(ord("l"), ord("i"), ord("s"), ord("t"))
