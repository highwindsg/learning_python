#!/usr/bin/env python3

# Storing lists in a tuple.

# First we create two lists.
eights = ["Edgar Allan Poe",
          "Charles Dickens"
          ]

nines = ["Hemingway",
         "Fitzgerald",
         "Orwell"
         ]

# Then add the two lists above into a tuple and assign it to the var 'authors'.
authors = (eights, nines)
print(authors)
