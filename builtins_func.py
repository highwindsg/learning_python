#!/usr/bin/env python3

# How to use Python's already built in functions.

print(dir(__builtins__))    # This will print out a list of Python's already built ins functions.

print(pow(2, 10))           # This will use the 'pow()' built-in func to calc the power of 2^10.

print(len("Hello"))         # This will use the 'len()' built-in func to find out the length of the word.

print(help(max))            # Using the 'help()' built-in func will help to display what the func does.
print(max(1,2,4,6,9))

