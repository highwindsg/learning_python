#!/usr/bin/env python3

def lbs_to_kg(weight):      # Define a func method named 'lbs_to_kg' with param
                            # weight.
    return weight * 0.45    # The value of weight multiply by 0.45 which
                            # converts to kilogram, and return the answer to the
                            # func method.

def kg_to_lbs(weight):      # Define a func method named 'kg_to_lbs' with param
                            # weight.
    return weight / 0.45    # The value of weight divided by 0.45 which                  
                            # converts to lbs (pound), and return the answer to
                            # the func method.

