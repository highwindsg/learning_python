#!/usr/bin/env python3

# Using the max() builtin func to find the largest number entered.
x = float(input("Enter 1st number: "))
y = float(input("Enter 2nd number: "))
z = float(input("Enter 3rd number: "))

print("The max value is: ", max(x, y, z))   # Print out the largest number by using the
                                            # max() func and parsing in the three params of var x, y, and z.
input("Press any key and/or ENTER to exit ")    # The input() func can also be used here to end the program.

