#!/usr/bin/env python3

while True:
    print("This phrase will always print.")
    break   # The 'break' statement will stop the sequence in the while
            # loop and proceed to line 10.
    # continue  # The 'continue' statement will cause the sequence
                # to go back to the top of the while loop and print
                # line 4, resulting in an infinite loop.
    print("Does this phrase print?")

print("We are done with the while loop.")

