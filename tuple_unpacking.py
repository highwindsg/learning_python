#!/usr/bin/env python3

# Create a var with items in a tuple.
coordinates = (1, 2, 3)
x, y, z = coordinates   # Unpacking the tuple of 1, 2, 3 respectively into x, y, z.
print(x)
print(y)
print(z)

# Create a var with items in a list.
fruits = ["apple", "pear", "guava"]
a, b, c = fruits        # Unpacking the list of fruits into a, b, c, respectively.
print(c)
print(b)
print(a)

