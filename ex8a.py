formatter = "{} {} {} {}"

print(formatter.format("d", "r", "m", "f"))
print(formatter.format("Do", "Re", "Me", "Fa"))
print(formatter.format("The", "sound", "of", "music"))
print(formatter.format(formatter, formatter, formatter, formatter))
print(formatter.format(
    "There was a time",
    "when people say",
    "Singapore wouldn't make it",
    "but we did."
))
